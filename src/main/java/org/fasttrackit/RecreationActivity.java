package org.fasttrackit;

public class RecreationActivity {

    String name;
    String type;

    public RecreationActivity(String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
